require 'spec_helper'

describe ShortUrl do
	it 'should accept a long URL and provide the short URL'

	it 'should accept a short URL and provide the short URL'

	it 'should be repeatable'

	it 'should differ by at least two characters from all other possible short URLs'

	it 'should handle common transcription ambiguities'

	it 'should provide a management key when a short URL is created'

	it 'should delete the short URL when a management key and short URL are given'

	it 'should update the short URL when a managment key, old short URL and new short URL are given'
end
